// Data type double
 #include<iostream>
using namespace std;
int main()
{
    double a=0.1;
    double b=0.2;
    double c=0.3;
    double x=a+b;
    cout<<"value of a+b is "<<x<<endl;
    cout<<"value for c is "<<c<<endl;
    if(x==c)
    cout<<"value are equal";
    else
    cout<<"value are not equal";
    return 0;
}



